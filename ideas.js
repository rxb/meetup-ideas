export const venueIdeas = (() => {

	const cats = {};
	for(cat in foursquareCategories){
		cats[cat] = {
			idea: cat,
			categories: [ cat ],
			description: '',
			radius: 8000,
			used: false
		}
	}

	ideas.forEach((idea)=>{
		idea.categories.forEach((cat)=>{
			const thisCat = cats[cat];
			thisCat.used = true;
			thisCat.description += `${idea.idea}, `;
			thisCat.radius = Math.max(idea.radius, thisCat.radius);
		});
	});

	const catIdeas = Object.keys(cats).map((key, index) => {
		return cats[key];
	}).filter((idea)=>{
		return idea.used;
	});

	return catIdeas;

})();


